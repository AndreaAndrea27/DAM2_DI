    private void refrescarTabla(){
        
        //creamos el modelo de tabla
        DefaultTableModel dtm= new DefaultTableModel();
        //le a√±adimos las cabeceras
        dtm.setColumnIdentifiers(new String[]{"Nombre","apellido","fecha"});
        //extraigo de la lista todos los contactos para meterlos en la tabla
        List<Contacto> listaContactos=LogicaNegocio.gestListaContactos();
        //hago un forech para que recorra la lista y los vaya grabando en la tabla
        for (Contacto contacto : listaContactos) {
            
            dtm.addRow(contacto.arrayContacto());
        }
        
        
        //le asignamos el modelo a nuestra tabla
        jTable_tabla.setModel(dtm);
        
        //para ordenar 
         TableRowSorter<javax.swing.table.TableModel> elQueOrdena= new TableRowSorter<>(dtm);
        jTable_tabla.setRowSorter(elQueOrdena);
        
        
    }

